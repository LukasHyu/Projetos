public class Ex06 {
    public static void main(String[] args) {
        int num1 = 125; //Define a variavel num1 com o valor de 125
        int num2 = 24;  //Define a variavel num2 com o valor de 24  

        System.out.println(num1 + " + " + num2 + " = " + (num1 + num2)); //Calcula o valor de num1 + num2 e mostra no console
        System.out.println(num1 + " - " + num2 + " = " + (num1 - num2)); //Calcula o valor de num1 - num2 e mostra no console
        System.out.println(num1 + " x " + num2 + " = " + num1 * num2); //Calcula o valor de num1 * num2 e mostra no console
        System.out.println(num1 + " / " + num2 + " = " + num1 / num2);  //Calcula o valor de num1 / num2 e mostra no console
        System.out.println(num1 + " mod " + num2 + " = " + num1 % num2); //Calcula o valor de num1 % num2 e mostra no console

    }
    
}
