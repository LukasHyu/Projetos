public class Ex11 {

    private static final double radius = 7.5; //Define a variavel radius com o valor de 7.5
    public static void main(String[] args) {
        double perimetro = 2 * Math.PI * radius; //Calcula o perimetro do circulo

        double area = Math.PI * radius * radius; //Calcula a area do circulo

        System.out.println("Perimetro = " + perimetro); //Mostra no console o valor do perimetro
        System.out.println("Area = " + area); //Mostra no console o valor da area

        
    }
}
